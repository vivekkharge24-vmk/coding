// SizeOf Datatype Program
#include <iostream>
using namespace std;
int main()
{
    cout << "All Data Types Bytes Size" << endl;
    cout << "int : " << sizeof(int) << endl;
    cout << "char : " << sizeof(char) << endl;
    cout << "float : " << sizeof(float) << endl;
    cout << "double : " << sizeof(double) << endl;
    cout << "string : " << sizeof(string) << endl;
}