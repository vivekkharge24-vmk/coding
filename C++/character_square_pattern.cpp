// Characters Square Pattern Print Program
#include <iostream>
using namespace std;
int main()
{
    cout << "Characters square pattern" << endl;
    for (int i = 1; i <= 5; i++)
    {
        for (char j = 'A'; j <= 'E'; j++)
        {
            cout << j << " ";
        }
        cout << endl;
    }
}
